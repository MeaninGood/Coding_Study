# 22113번_창영이와 버스

## 창영이가 사는 도시에는 N개의 버스
## S번 버스에서 E번 버스로 환승하기 위해서는 AS,E원이 필요
## 창영이가 출근하기 위해 이용하는 M개의 버스가 환승하는 순서대로 주어짐
## 모든 버스끼리의 환승 요금이 주어짐
## 창영이가 한 번 출근하는데 지불해야하는 환승 요금의 합 구하기
## 최초에 탑승할 때 지불하는 버스 요금은 제외

'''
# 첫째 줄에 도시에 존재하는 버스의 개수 N, 창영이가 이용하는 버스의 개수 M
# 둘째 줄에 창영이가 이용하는 M개 버스의 번호가 순서대로 주어짐
# 버스 번호는 중복되지 않음
# 셋째 줄부터 N개의 줄에 걸쳐 버스의 환승 요금에 대한 정보가 주어짐
# S번째 줄의 E번째 수는 AS,E를 의미
# AS,E는 AE,S와 다를 수 있음 
## 필요한 환승 요금의 총합을 출력

(입력)
4 3
3 1 2
0 2 3 4
5 0 7 8
1 2 0 4
5 6 7 0

(출력)
3

'''